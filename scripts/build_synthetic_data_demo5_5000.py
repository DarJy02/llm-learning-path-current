def choose_style(rng, category):
    weighted_styles = STYLE_WEIGHTS
    if category == "商品咨询":
        weighted_styles = [(s, w) for s, w in STYLE_WEIGHTS if s != "empathy"]
    if category == "发票":
        weighted_styles = [(s, w) for s, w in STYLE_WEIGHTS if s != "empathy"]
    styles, weights = zip(*weighted_styles)
    return rng.choices(styles, weights=weights, k=1)[0]


def render(style, answer, action, note, multi=False):
    prefix = "结合前面的情况，" if multi and style in {"direct", "boundary"} else ""
    if style == "short":
        return f"{answer}{action}"
    if style == "confirm":
        lead = "先确认关键点：" if not multi else "那先抓关键点："
        return f"{lead}{note}{answer}{action}"
    if style == "risk":
        return f"{answer}{action}这里要注意，{note}"
    if style == "empathy":
        lead = "这种情况确实会影响使用。" if not multi else "您这个情况确实需要尽快处理。"
        return f"{lead}{answer}{action}{note}"
    return f"{prefix}{answer}{action}{note}"


def make_from_blueprint(category, scenario, product, detail, style, turn_type, idx, rng):
    topic, q_tmpl, context_tmpl, follow, answer, action, note = scenario
    question = q_tmpl.format(product=product, detail=detail)
    context = context_tmpl.format(product=product, detail=detail)
    output = render(style, answer, action, note, multi=(turn_type == "multi"))
    if turn_type == "single":
        if category == "商品咨询":
            suffix = rng.choice(["", "下单前想确认清楚。", "麻烦帮我判断一下。", "我怕买错。"])
        elif category in {"售后服务", "退换货", "物流跟踪", "订单查询", "发票", "人工投诉"}:
            suffix = rng.choice(["", "麻烦帮我判断一下。", "我想确认怎么处理。", "现在不知道下一步怎么办。"])
        else:
            suffix = rng.choice(["", "麻烦帮我判断一下。", "我想确认一下规则。", "现在不知道下一步怎么办。"])
        context_text = context if context.endswith(("。", "？", "！")) else f"{context}。"
        instruction = f"{question}{context_text}{suffix}"
        history = []
    else:
        instruction = follow
        history = [
            [OPENERS[category], CLARIFY[category]],
            [context, rng.choice(ACKS)],
        ]
    return {
        "instruction": instruction,
        "input": "",
        "output": output,
        "system": SYSTEM,
        "history": history,
        "metadata": {
            "source": "synthetic_demo5_5000",
            "category": category,
            "turn_type": turn_type,
            "answer_style": style,
            "topic": topic,
            "product": product,
            "style_reference": "项目一/数据处理/电商客服合成数据风格参考.md",
            "sample_id": f"demo5_{turn_type}_{idx:04d}",
        },
    }


def generate_category(category, single_count, multi_count, rng):
    rows = []
    seen = set()
    scenarios = SCENARIOS[category]
    products = PRODUCTS[category]
    needed = [("single", single_count), ("multi", multi_count)]
    for turn_type, count in needed:
        i = 0
        attempts = 0
        while i < count:
            attempts += 1
            if attempts > count * 50:
                raise RuntimeError(f"too many duplicate attempts for {category}/{turn_type}")
            scenario = rng.choice(scenarios)
            topic = scenario[0]
            if topic in TOPIC_PRODUCT_DETAILS:
                product = rng.choice(list(TOPIC_PRODUCT_DETAILS[topic].keys()))
                detail = rng.choice(TOPIC_PRODUCT_DETAILS[topic][product])
            else:
                product = rng.choice(TOPIC_PRODUCTS.get(topic, products))
                detail = rng.choice(TOPIC_DETAILS.get(topic, DETAILS_BY_CATEGORY[category]))
            style = choose_style(rng, category)
            row = make_from_blueprint(category, scenario, product, detail, style, turn_type, i + 1, rng)
            key = (turn_type, row["instruction"], row["output"], tuple(tuple(h) for h in row["history"]))
            if key in seen:
                continue
            seen.add(key)
            rows.append(row)
            i += 1
    return rows


def build():
    rng = random.Random(20260428)
    data = []
    for category, (single_count, multi_count) in CATEGORY_TARGETS.items():
        data.extend(generate_category(category, single_count, multi_count, rng))
    rng.shuffle(data)
    for idx, row in enumerate(data, start=1):
        row["metadata"]["sample_id"] = f"demo5_{idx:04d}"
    return data


def validate(data):
    assert len(data) == 5000, len(data)
    assert sum(1 for row in data if not row["history"]) == 3500
    assert sum(1 for row in data if row["history"]) == 1500
    bad_terms = ["亲亲", "小妹", "么么", "mua", "还有其他可以帮您的吗", "您好，我来帮您查询一下"]
    text = json.dumps(data, ensure_ascii=False)
    for term in bad_terms:
        assert term not in text, term
    for row in data:
        assert set(["instruction", "input", "output", "system", "history"]).issubset(row)
        assert isinstance(row["history"], list)
        if row["history"]:
            assert len(row["history"]) == 2
            assert "优惠要以结算页和活动规则共同判断" not in json.dumps(row["history"], ensure_ascii=False)
            assert "发票要看订单状态、开票主体" not in json.dumps(row["history"], ensure_ascii=False)


def write_report(data):
    def count_by(fn):
        out = {}
        for row in data:
            key = fn(row)
            out[key] = out.get(key, 0) + 1
        return dict(sorted(out.items()))

    category_counts = count_by(lambda r: r["metadata"]["category"])
    turn_counts = count_by(lambda r: r["metadata"]["turn_type"])
