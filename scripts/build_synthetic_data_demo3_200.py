def make_single(case, idx):
    category, style, question, _context, _follow, answer, action, note = case
    output = render(style, answer, action, note, multi=False)
    return row(idx, "single", category, style, question, [], output)


def make_multi(case, idx):
    category, style, question, context, follow, answer, action, note = case
    history = build_history(category, question, context)
    output = render(style, answer, action, note, multi=True)
    return row(idx, "multi", category, style, follow, history, output)


def render(style, answer, action, note, multi):
    ctx = "结合前面的情况，" if multi and style in {"direct", "boundary"} else ""
    if style == "short":
        return f"{answer}{action}"
    if style == "direct":
        return f"{ctx}{answer}{action}{note}。"
    if style == "confirm":
        prefix = "先确认关键点：" if not multi else "那先抓关键点："
        return f"{prefix}{note}。{answer}{action}"
    if style == "risk":
        lead = "这一步先稳住：" if multi else ""
        return f"{lead}{answer}{action}这里要注意，{note}。"
    if style == "boundary":
        return f"{ctx}{answer}{action}{note}。"
    if style == "empathy":
        lead = "这种情况确实会影响使用。" if not multi else "您这个进度确实不太省心。"
        return f"{lead}{answer}{action}{note}。"
    if style == "light":
        return f"{answer}{action}{note}。"
    return f"{answer}{action}{note}。"


