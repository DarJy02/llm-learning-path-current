SYSTEM_PROMPT = (
    "你是京东电商场景的中文客服助手。"
    "请根据顾客问题和已有对话上下文，给出礼貌、准确、可执行的客服回复。"
    "回答应先解决用户问题，再给出必要的安抚和兜底说明，不要重复同一句话。"
)

PLACEHOLDER_KW = [
    '[订单x]', '[数字x]', '[金额x]', '[姓名x]',
    '[地址x]', '[站点x]', '[日期x]', '[时间x]',
    '[订单编号:', '[商品快照]',
]


def has_placeholders(text):
    for kw in PLACEHOLDER_KW:
        if kw in text:
            return True
    return False


def make_sample(instruction, output, history=None, category="", source="synthetic_v5"):
    return {
        "instruction": instruction,
        "input": "",
        "output": output,
        "system": SYSTEM_PROMPT,
        "history": history or [],
        "metadata": {
            "source": source,
            "category": category,
        },
    }


