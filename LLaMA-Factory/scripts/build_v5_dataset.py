# Step 6: Merge all
# ============================================================
random.seed(42)

seen_keys = set()
merged = []


def add_samples(samples, label):
    added = 0
    skipped = 0
    for item in samples:
        inst = item.get('instruction', '').strip()
        key = inst[:60]
        if key not in seen_keys:
            seen_keys.add(key)
            merged.append(item)
            added += 1
        else:
            skipped += 1
    print(f"  [{label}] added={added}, skipped_dup={skipped}")


add_samples(v3_clean, "v3_clean")
add_samples(v4_clean, "v4_clean")
add_samples(synthetic, "synthetic")

print(f"\n=== FINAL DATASET: {len(merged)} samples ===")

# Stats
cats = {}
has_history = 0
for item in merged:
    c = item.get('metadata', {}).get('category', 'unknown')
    cats[c] = cats.get(c, 0) + 1
    if item.get('history') and len(item['history']) > 0:
        has_history += 1

print(f"With history: {has_history} ({has_history/len(merged)*100:.0f}%)")
print(f"\nCategory distribution:")
for k, v in sorted(cats.items(), key=lambda x: -x[1]):
    print(f"  {k}: {v}")

