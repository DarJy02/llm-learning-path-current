# Step 1: Load v3
# ============================================================
with open('data/ecommerce_customer_sft_v3_real.json', 'r', encoding='utf-8') as f:
    v3 = json.load(f)
print(f"[v3] loaded {len(v3)} samples")

# ============================================================
# Step 2: Load & filter v4
# ============================================================
with open('data/ecommerce_customer_sft_v4_merged.json', 'r', encoding='utf-8') as f:
    v4 = json.load(f)
print(f"[v4] loaded {len(v4)} samples")

JUNK_OUTPUTS = {
    '好的呢', '嗯', '哦', '是的', '对的', '亲', '亲亲',
    '在的呢', '您好', '你好', '稍等', '稍等哦', '请稍等',
    '好的', '嗯嗯', 'ok', 'OK', '好的哦', '是的呢',
}

v4_clean = []
stats = {'placeholder': 0, 'short': 0, 'long': 0, 'junk': 0, 'short_inst': 0}

for item in v4:
    inst = item.get('instruction', '')
    out = item.get('output', '')
    src = item.get('metadata', {}).get('source', '')

    if has_placeholders(inst) or has_placeholders(out):
        stats['placeholder'] += 1
        continue
    if len(out) < 25:
        stats['short'] += 1
        continue
    if len(out) > 350:
        stats['long'] += 1
        continue
    if out.strip() in JUNK_OUTPUTS:
        stats['junk'] += 1
        continue
    if len(inst.strip()) < 4:
        stats['short_inst'] += 1
        continue

    item['system'] = SYSTEM_PROMPT
    v4_clean.append(item)

print(f"[v4] kept {len(v4_clean)} clean, rejected: {stats}")

# ============================================================
# Step 3: Normalize v3
# ============================================================
v3_clean = []
v3_rej = 0
for item in v3:
    out = item.get('output', '')
    inst = item.get('instruction', '')
    if not out.strip() or len(out.strip()) < 10:
        v3_rej += 1
        continue
    if has_placeholders(out) or has_placeholders(inst):
        v3_rej += 1
        continue
    item['system'] = SYSTEM_PROMPT
    v3_clean.append(item)
print(f"[v3] kept {len(v3_clean)} clean (rejected {v3_rej})")

